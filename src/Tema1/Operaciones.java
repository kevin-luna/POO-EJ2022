package Tema1;

public class Operaciones {
    public static int suma(int a,int b){
        return a+b;
    }

    public static int multiplicar(int a,int b){
        return a*b;
    }

    public static int divide(int a,int b){
        return a/b;
    }

    public static int resta(int a,int b){
        return a-b;
    }
}
